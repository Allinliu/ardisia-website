<h3>Creating and Using the Filters</h3>
<p>
	Create and use the filters just like typical Flex filters, such as the Glow 
	filter, the DropShadow filter, etc. They can be applied to DisplayObjects.
</p>
<p>
	These filters all extend Spark filters.
</p>

<h3>Strength</h3>
<p>
	Some filters have a "strength" property that changes the intensity of the 
	effect. Most filters that have a "strength" property can vary the strength 
	from 0 to 100 with 50 being the default.  The exception is the "HueFilter", 
	which varies from -180 to 180.
</p>
<p>
	See the ASDocs for each filter.
</p>

