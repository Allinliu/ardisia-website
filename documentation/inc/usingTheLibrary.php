<h3>Setup</h3>
<p>
	To use the Ardisia Component Library in your projects, you can either (A) 
	use the included SWC file in your projects or (B) create your own SWC 
	library file by compiling the library source code into a SWC library file.
</p>
<p>
	If you create the SWC file yourself, it is very important that all the 
	included assets are embedded into the SWC file.  In other words, all 
	uncompiled assets like images, stylesheets, etc., should be embedded into 
	the SWC file for proper operation.
</p>
<p>
	For more information on SWC files, see:
	<ul>
		<li>General Information: <a target="_blank" href="http://help.adobe.com/en_US/flex/using/WS2db454920e96a9e51e63e3d11c0bf69084-7fd3.html">this link</a></li>
		<li>Flash Builder Specific: <a target="_blank" href="http://help.adobe.com/en_US/Flex/4.0/UsingFlashBuilder/WS6f97d7caa66ef6eb1e63e3d11b6c4d0d21-7fe6.html">this link</a></li>
	</ul>
</p>

