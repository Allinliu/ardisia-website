<h3>Overview</h3>
<p>
	ToggleSwitch component designed for desktop use.
</p>

<h3>Creating the ToggleSwitch Component</h3>
<p>
	The ToggleSwitch component extends ToggleButton and can be added to any 
	container that implements IVisualElementContainer.
</p>

<h3>Using the ToggleSwitch Component</h3>
<p>
	Functions just like a ToggleButton.  Most of the logic is handled in the 
	skin, but the labels can be set via properties.
</p>

<h3>Layout</h3>
<p>
	The default skin layout can accurately measure the toggle button based on 
	the labels.  It is not necessary to set explicit dimensions.
</p>

<h3>Animations &amp; Effects</h3>
<p>
	The thumb will animate by default. Customize the skin to change the 
	animation.
</p>

<h3>Accessibility</h3>
<p>
	Field can be tabbed to and selected/de-selected via the SPACE key when 
	focused just like a vanilla ToggleButton.
</p>

<h3>Themes &amp; Skinning</h3>
<p>
	A skin is provided for the Spark theme.
</p>

