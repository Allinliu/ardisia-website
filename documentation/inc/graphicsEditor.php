<h3>Overview</h3>
<p>
	The graphics editor is incomplete and the code is in an alpha state.
</p>
<p>
	Supports 14 interaction modes:
</p>
<ul>
	<li>NONE</li>
	<li>SELECT_FRONT</li>
	<li>SELECT_BEHIND</li>
	<li>CROP</li>
	<li>BITMAP_LASSO</li>
	<li>WAND</li>
	<li>BRUSH</li>
	<li>FLOOD_FILL</li>
	<li>DISTORT</li>
	<li>VECTOR</li>
	<li>TEXT</li>
	<li>ZOOM_IN</li>
	<li>ZOOM_OUT</li>
	<li>GRABBER</li>
</ul>

<h3>Features</h3>
<ul>
	<li>Undo / redo support.</li>
	<li>Brush hardness, alpha support.</li>
	<li>Selection, rotation, and depth support.</li>
	<li>Wand and Flood fill: tolerance, AA, and alpha support.</li>
	<li>Eraser and brushes supporting anti-alias, alpha, and weight.</li>
	<li>Brush Modes: draw, erase, blur, sharpen, pinch, bloat.</li>
	<li>3 vector shapes are supported: LINE, RECTANGLE, and ELLIPSE.</li>
	<li>2 brush shapes are currently supported: SQUARE and CIRCLE.</li>
	<li>Marching Ants animation support for the WAND and BITMAP_LASSO mode.</li>
	<li>Bitmap selection tolerance.</li>
	<li>Adding, removing, and editing text.</li>
	<li>Zooming in/out on the cursor position.</li>
	<li>Selection lassos.</li>
	<li>Grouping support.</li>
	<li>Auto scrolling.</li>
	<li>Fitting content to the canvas, canvas transparency.</li>
	<li>Bitmap exporting.</li>
</ul>

